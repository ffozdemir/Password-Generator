const firstPass = document.querySelector("#first-pass");
const secondPass = document.querySelector("#second-pass");

function getRandomIndex(array) {
  return Math.floor(Math.random() * array.length);
}

function getPass() {
  let firstPassText = "";
  let secondPassText = "";

  for (let i = 0; i < 15; i++) {
    const randomIndexOne = getRandomIndex(characters);
    const randomIndexTwo = getRandomIndex(characters);
    firstPassText += characters[randomIndexOne];
    secondPassText += characters[randomIndexTwo];
  }

  firstPass.textContent = firstPassText;
  secondPass.textContent = secondPassText;
}
